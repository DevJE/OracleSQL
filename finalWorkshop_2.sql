-- QUESTION 21
SELECT  BOOK_NM 도서명, STOCK_QTY 재고수량, PRICE "가격(Org)",
        (PRICE * 0.8) "가격(New)"
FROM    TB_BOOK
WHERE   TO_CHAR(SYSDATE, 'YYYY') - TO_CHAR(ISSUE_DATE, 'YYYY') > 30
        AND STOCK_QTY > 89
ORDER BY 2 DESC, 4 DESC, 1;

