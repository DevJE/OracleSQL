FROM     ( SELECT  WRITER_NM NM,
                   OFFICE_TELNO OT,
                   HOME_TELNO HT,
                   MOBILE_NO MN,
                   RANK() OVER (ORDER BY WRITER_NM ASC) RNK
           FROM TB_WRITER
