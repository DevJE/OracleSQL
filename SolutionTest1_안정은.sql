-- QUESTION 08
SELECT   STUDENT_NAME 학생이름,
         TO_CHAR(ENTRANCE_DATE, 'RRRR"년"MM"월"DD"일"') 입학일자,
         FLOOR( MONTHS_BETWEEN (SYSDATE, ENTRANCE_DATE) / 12) || '년' "입학후기간(년)" 
