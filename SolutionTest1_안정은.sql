SELECT    계열, 학과이름, COUNT(*) 학생수
FROM      ( SELECT  DEPARTMENT_NAME 학과이름, 
                    DEPARTMENT_NO, 
                    CATEGORY 계열
            FROM    TB_DEPARTMENT
            WHERE   CATEGORY = '예체능' OR CATEGORY = '의학') F
LEFT JOIN TB_STUDENT S ON (S.DEPARTMENT_NO = F.DEPARTMENT_NO) 
GROUP BY  계열, 학과이름
ORDER BY  1, 3 DESC;

